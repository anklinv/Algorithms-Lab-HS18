// BGL Graph definitions
// =====================
// Graph Type with nested interior edge properties for Flow Algorithms
typedef	boost::adjacency_list_traits<boost::vecS, boost::vecS, boost::directedS> Traits;
typedef boost::adjacency_list<boost::vecS, boost::vecS, boost::directedS, boost::no_property,
	boost::property<boost::edge_capacity_t, long,
		boost::property<boost::edge_residual_capacity_t, long,
			boost::property<boost::edge_reverse_t, Traits::edge_descriptor> > > >	Graph;
// Interior Property Maps
typedef	boost::property_map<Graph, boost::edge_capacity_t>::type		EdgeCapacityMap;
typedef	boost::property_map<Graph, boost::edge_residual_capacity_t>::type	ResidualCapacityMap;
typedef	boost::property_map<Graph, boost::edge_reverse_t>::type		ReverseEdgeMap;
typedef	boost::graph_traits<Graph>::vertex_descriptor			Vertex;
typedef	boost::graph_traits<Graph>::edge_descriptor			Edge;
typedef	boost::graph_traits<Graph>::edge_iterator			EdgeIt;


// Custom Edge Adder Class, that holds the references
// to the graph, capacity map and reverse edge map
// ===================================================
class EdgeAdder {
	Graph &G;
	EdgeCapacityMap	&capacitymap;
	ReverseEdgeMap	&revedgemap;

public:
	// to initialize the Object
	EdgeAdder(Graph & G, EdgeCapacityMap &capacitymap, ReverseEdgeMap &revedgemap):
		G(G), capacitymap(capacitymap), revedgemap(revedgemap){}

	// to use the Function (add an edge)
	void addEdge(int from, int to, long capacity) {
		Edge e, rev_e;
		bool success;
		boost::tie(e, success) = boost::add_edge(from, to, G);
		boost::tie(rev_e, success) = boost::add_edge(to, from, G);
		capacitymap[e] = capacity;
		capacitymap[rev_e] = 0; // reverse edge has no capacity!
		revedgemap[e] = rev_e;
		revedgemap[rev_e] = e;
	}
};


using namespace std;

void testcase() {
	int nr_zones, nr_jobs;
	cin >> nr_zones >> nr_jobs;
	
	Graph G(nr_zones + nr_jobs);
	EdgeCapacityMap capacitymap = boost::get(boost::edge_capacity, G);
	ReverseEdgeMap revedgemap = boost::get(boost::edge_reverse, G);
	ResidualCapacityMap rescapacitymap = boost::get(boost::edge_residual_capacity, G);
	EdgeAdder eaG(G, capacitymap, revedgemap);
	
	int source = add_vertex(G);
	int target = add_vertex(G);
	
	for (int i = 0; i < nr_zones; ++i) {
		int cost; cin >> cost;
		eaG.addEdge(i + nr_jobs, target, cost);
	}
	
	int totalReward = 0;
	for (int i = 0; i < nr_jobs; ++i) {
		int reward; cin >> reward;
		eaG.addEdge(source, i , reward);
		totalReward += reward;
	}

	for (int i = 0; i < nr_jobs; ++i) {
		int n; cin >> n;
		for (int j = 0; j < n; ++j) {
			int z; cin >> z;
			eaG.addEdge(i, nr_jobs + z, INT_MAX);
		}
	}
	
	int flow = boost::push_relabel_max_flow(G, source, target);

	cout << totalReward - flow << endl;
}

int main() {
	int T; cin >> T;
	while(T--)
		testcase();
}